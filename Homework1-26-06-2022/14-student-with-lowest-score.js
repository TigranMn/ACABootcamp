function getTheLowestScoredStudent(examScoresList) {
  let lowestScore = examScoresList[0].score;
  let lowestScoredStudent = examScoresList[0].studentName;
  for (let i = 1; i < examScoresList.length; i++) {
    let currScore = examScoresList[i].score;
    let currStudent = examScoresList[i].studentName;
    if (lowestScore > currScore) {
      lowestScore = currScore;
      lowestScoredStudent = currStudent;
    }
  }
  return lowestScoredStudent;
}

